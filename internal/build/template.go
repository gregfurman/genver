package build

import "text/template"

const templateContent = `// Code generated by github.com/gregfurman/genver@{{ .GenVerVersion }} DO NOT EDIT.
package {{.Package}}
{{ range .Dependencies }}
// Currently using {{ .Path }} {{ .Version }} 
const (
    {{ .Name }}_Version = "{{ .Version }}"
    {{ .Name }}_Path    = "{{ .Path }}"
)
{{ end }}`

func retrieveTemplate() (*template.Template, error) {
	tmpl, err := template.New("genver").Parse(string(templateContent))
	if err != nil {
		return nil, err
	}

	return tmpl, nil
}
